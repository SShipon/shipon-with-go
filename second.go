package main
import ("fmt")
// data type in golang 
func main() {
  var a bool = true    // Boolean
  var b int = 5        // Integer
  var c float32 = 3.14 // Floating point number
  var d string = "Hi!" // String

  fmt.Println("Boolean: ", a)
  fmt.Println("Integer: ", b)
  fmt.Println("Float:   ", c)
  fmt.Println("String:  ", d)
}